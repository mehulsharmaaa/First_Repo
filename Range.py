#RANGE FUNCTION:

for x in range(9): #Range basically specifies returns a sequence of numbers, starting from 0 by default, and increments by 1 (by default), and stops before a specified number
    print(x)
for y in range(2,5): #Starting point is specified too
    print(y)
for z in range(2,20,2): #Starting, end and increment is specified
    print(z)

L = ['appple', 'mangoes', 'bananas', 'oranges', 'cherries']
for items in range(1,3): 
    print(L[items])